package variables;

public class Main {

	public static void main(String[] args) {
		//Java is case sensitive. 
		
		int ogrenciSayisi = 12;
		String mesaj = "Öğrenci sayısı: ";
		//camelCase is used to assign variables. First word starts with lower case and the others with upper case.
		System.out.println(mesaj + ogrenciSayisi);
		System.out.println(mesaj + ogrenciSayisi);
		System.out.println(mesaj + ogrenciSayisi);
		System.out.println("Öğrenci sayım :" + ogrenciSayisi);
		//reusability, write once run anywhere, tekrar kullanılabilirlik. 
		
		

	}
	


}
